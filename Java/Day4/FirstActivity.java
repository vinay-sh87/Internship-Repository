public class FirstActivity{
	public static void main(String args[]){
		// Tilde (~) : increases of value by 1 and multiplies with -1
		int a = -10;
		System.out.println("The result of Tilde operation :: "+(~a));

		// Not Operator : inverts the value
		boolean flag = true;
		System.out.println("The result of not operation :: "+(!flag));
	}
}