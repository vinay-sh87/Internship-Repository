public class SecondActivity{
	public static void main(String args[]){
		int a = 10;
		System.out.println(a++); // a(10) = a(10) + 1(11) // nearest value from the assginment operator will be stored
		System.out.println(a);
		int b = 20;
		System.out.println(++b); // b(20) + 1(21) = b(21); // here too
	}
}