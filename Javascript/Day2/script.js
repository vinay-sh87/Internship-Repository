/* ways to declare a variable 
var, let and const
var : can be redeclared , function scoped 
let : can't be redeclared , block scoped 
const : value can't be re assigned or redeclared ones initialized , block scoped
*/

// typeof is a operator that works on a single operand 
console.log(typeof 5 + 2); // number + 2

let user; // declaration
user = "Pearson"; // initialization
console.log(user);

